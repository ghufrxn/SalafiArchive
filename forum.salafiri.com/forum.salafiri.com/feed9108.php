The means of attaining firmness upon the Deen – Shaykh Fawzaan حفظه الله<br /><br />Shaykh Fawzaan حفظه الله was asked: We ask your Excellency to mention to us the means of attaining firmness upon the Deen while there is frequent relapsing in this age?<br /><br />The Shaikh حفظه الله replied,”The means are many, and the major among them are: Taqwaa of Allaah سبحانه وتعالى (God-consciousness), fearing Allaah تعالى,likewise, asking the people of knowledge when you are confused (or doubtful about a matter), accompanying the righteous, remembering Allaah much and reciting the Qur’aan more, strictly guarding the prayer (and establishing it) with the Jamaa`ah . All of these are from the means of attaining firmness upon the Deen.<br /><br />Source:<br /><a href="http://alfawzan.af.org.sa/node/14584" class="postlink">http://alfawzan.af.org.sa/node/14584</a><p>Statistics: Posted by <a href="https://forum.salafiri.com/memberlist.php?mode=viewprofile&amp;u=56">Shehzad Sattar</a> — Fri Sep 09, 2016 2:24 am</p><hr />
