<title type="html"><![CDATA[The Ruling On the Companions Saying Something is the Sunnah.]]></title>

<content type="html" xml:base="https://forum.salafiri.com/viewtopic.php?t=958&amp;p=1002#p1002"><![CDATA[
<span style="font-size: 150%; line-height: normal">Imam Badi al-Din Shah al-Rashidi al-Sindhi said, &quot;The 7th Chapter: The Saying of a Companion that such and such is the Sunnah.<br /><br />The people of knowledge differ over this affair whether this mawquf – stopped at a companion or marfu – ie rasied to the Messenger of Allah (Sallalahu Alayhi Wasallam). After researching and contemplating this issue I have come to the conclusion that it falls under the ruling of being marfu because the the companions saying something is the Sunnah can only but mean the Sunnah of the Messenger of Allah (Sallalahu Alayhi Wasallam). Khatib in Kifayah and Ibn Salah in his al-Muqaddimah have alluded to this and Zaila’i and others have also cited this from Ibn Abdul Barr.” (Rafa’ al-Irtiyab An Hukm al-Ashab (p.7) (Manuscript form)</span><p>Statistics: Posted by <a href="https://forum.salafiri.com/memberlist.php?mode=viewprofile&amp;u=48">AbuKhuzaimahAnsari</a> — Wed Jan 04, 2017 11:41 pm</p><hr />
