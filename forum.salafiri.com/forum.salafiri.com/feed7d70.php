<title type="html"><![CDATA[Preferring to Spend on One’s Poor Parents Over Slaughtering Sheep for an ‘Aqeeqah]]></title>

<content type="html" xml:base="https://forum.salafiri.com/viewtopic.php?t=469&amp;p=499#p499"><![CDATA[
<span style="color: #0000FF">Shaykh ‘Abdur-Rahmaan ibn Naasir as-Sa’dee (d.1376)</span>, may Allaah have Mercy on him, was asked, “If someone has poor parents, should preference be given to spending on their needs over slaughtering sheep for an <em>‘Aqeeqah</em> (i.e. for a newborn child)”?<br /><br />His answer:<br /><br />When a person has poor parents, their needs are given preference over (slaughtering for) an ‘Aqeeqah, since taking care of their needs is an obligation, while an ‘Aqeeqah is Sunnah (recommended).  This is when there is no way to do both.<br /><br /><span style="color: #0000FF">Source:</span> <em>Al-Fataawee As-Sa’diyyah (p.269)</em><br /><br /><span style="color: #0000FF">Source:</span> <!-- m --><a class="postlink" href="http://www.bakkah.net/en/preferring-to-spend-on-ones-poor-parents-over-having-an-aqeeqah.htm">http://www.bakkah.net/en/preferring-to- ... qeeqah.htm</a><!-- m --><p>Statistics: Posted by <a href="https://forum.salafiri.com/memberlist.php?mode=viewprofile&amp;u=56">Shehzad Sattar</a> — Tue Sep 27, 2016 12:14 am</p><hr />
