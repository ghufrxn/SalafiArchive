<title type="html"><![CDATA[3 Companions - Wealth, Family &amp; Deeds]]></title>

<content type="html" xml:base="https://forum.salafiri.com/viewtopic.php?t=1583&amp;p=1720#p1720"><![CDATA[
Anas (Radī Allāhū ʾAnhū) narrated that the Prophet (Sallalāhū ʾAlayhī Wa Sallam) said,<br /><br /><strong>&quot;There is no person except that he has 3 companions. The 1st companion says, ʾWhatever you spend is for you, and whatever you withhold is not for youʾ, and this is his wealth. The 2nd says, ʾI am with you, but when you come to the door of the King (Allāh), I will return and leave youʾ, and this is his family and nobility. The 3rd says, ʾI am with you wherever you enter, and wherever you goʾ, and this is his deeds. The person will say: ʾYou were the one I gave the least consideration from these 3.ʾ&quot;</strong><br /><br /><em>Mustadrak al-Hākim (1/371). Hākim decalred it Sahīh and Dhahabī agreed with him. Musnad at-Tayālisī no.2013 and Kashf al-Astār by Bazār no.3229</em><p>Statistics: Posted by <a href="https://forum.salafiri.com/memberlist.php?mode=viewprofile&amp;u=59">Abu Ubaydah</a> — Mon Jun 15, 2020 11:49 pm</p><hr />
