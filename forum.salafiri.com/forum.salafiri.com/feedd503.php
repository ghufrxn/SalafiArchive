<title type="html"><![CDATA[The Two Types of Knowledge By Shaykh al-Islaam Ibn Taymiyyah]]></title>

<content type="html" xml:base="https://forum.salafiri.com/viewtopic.php?t=74&amp;p=78#p78"><![CDATA[
It was said (by Hassan al-Basree): <em>“Knowledge is of two types: knowledge of the heart and knowledge of the tongue. As for knowledge of the heart, then this is beneficial; and knowledge of the tongue is an establishment of the proof (of the truth from Allah) upon His slaves.”</em><br /><br />Thus the scholar who understands with his heart is not like the Khateeb (one who gives sermons) who speaks with his tongue. For verily a person can understand and comprehend great matters with his heart, but he may not be able to address it to other than him.<br /><br />On the other hand, a person may be able to speak of many matters of the knowledge of the heart and its conditions, yet he himself may be completely unaware of the meaning of these matters.<br /><br /><strong>(Durr at-Ta’aarudh by Shaykh al-Islaam Ibn Taymiyyah, 8:453,454) (Translation and additional comments by Ibn Hussain for Markazus-Sunnah Leicester, 1435)</strong><p>Statistics: Posted by <a href="https://forum.salafiri.com/memberlist.php?mode=viewprofile&amp;u=56">Shehzad Sattar</a> — Wed Aug 24, 2016 11:34 pm</p><hr />
