<title type="html"><![CDATA[It is a must to use all of the Texts together]]></title>

<content type="html" xml:base="https://forum.salafiri.com/viewtopic.php?t=395&amp;p=424#p424"><![CDATA[
It is necessary and obligatory to use all of the Texts relating to an issue, because all of them are from Allah Ta'aalaa. Allah 'Azza wa Jal said, paraphrased: {<strong>Follow what has come down to you from your Lord</strong>} [Al-A'raf, 7:3], and Allah Ta'aalaa said, paraphrased: {<strong>And what the Messenger has given you, take</strong>] [Al-Hashr, 59:7]. Therefore it is not allowed to leave a Text, claiming that it is abrogated, nor is it allowed to leave an additional ruling found in the generality of the Text, claiming that it is restricted or specified, except when there is evidence from the Book, Sunnah, and/or certain Ijma' to necessitate doing so. Allah 'Azza wa Jal said, paraphrased: {<strong>Say: Bring your Burhan (clear, undeniable proof) if you are truthful</strong>} [Al-Baqarah, 2:111]. Wallahu a'lam.<p>Statistics: Posted by <a href="https://forum.salafiri.com/memberlist.php?mode=viewprofile&amp;u=110">مذهب العمل بالحديث</a> — Sat Sep 17, 2016 12:15 am</p><hr />
